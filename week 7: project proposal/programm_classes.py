#Some of the classes will include

#Spot- This class represents one block of the 8*8 grid and another optional piece.

#Piece- This class is the basic building block of the system, every piece will be placed on a spot.

#From class piece, we can also create other class children which include, King, Queen, Knight and Bishop. These are the abtract operations in the game.

#Board- The class board is the 8*8 of the boxes containing all the active chess pieces

#Player- Player class represents the partcisipants playing the game and are the ones making the game complete.

#Move- This class represents the game moves containing the starting and ending spots. This class will also help to keep track of the player who made the move.
